# Now do the actual mod
for (label, balance, partset, weap_type) in sorted(weapons):
    mod.comment(label)

    part_data = get_partlist(weap_type)

    # For some reason the Faulty Star hotfixes don't like to apply properly with
    # Mod.PATCH -- they end up *blanking out* the generic parts lists, rather than
    # adding anything in.  It actually ends up breaking even the Raid1+Event2
    # anointment expansions.  Using a LEVEL based hotfix works for the Balance
    # but weirdly still blanks out the PartSet.  Fortunately, by that time, only
    # the Balance matters, so it happens to still work anyway.  Still: weird.
    if label == 'Faulty Star':
        hf_type = Mod.LEVEL
        hf_target = 'MatchAll'
    else:
        hf_type = Mod.PATCH
        hf_target = ''

    # First the Balance
    mod.reg_hotfix(hf_type, hf_target,
            balance,
            'RuntimeGenericPartList.bEnabled',
            'True')
    mod.reg_hotfix(hf_type, hf_target,
            balance,
            'RuntimeGenericPartList.PartList',
            part_data)

    # Then the PartSet
    mod.reg_hotfix(hf_type, hf_target,
            partset,
            'GenericParts.bEnabled',
            'True')
    mod.reg_hotfix(hf_type, hf_target,
            partset,
            'GenericParts.PartList',
            part_data)

    mod.newline()

mod.close()
